    public void calc() {
        // variaveis
    Double Altura;
    Double Peso;
    Double Imc;
    String mensagem;
    
        // captura do que foi digitado
    Altura = Double.parseDouble(Txheight.getText());
    Peso = Double.parseDouble(Txweight.getText());
    
        // calculo
    Imc = Peso/(Altura/100 * Altura/100);
    
    if (Imc < 18.5) {
        mensagem = " Baixo peso(grau I)";
    }else if (Imc < 24.9) {
        mensagem = " Normal (?)";
    }else if (Imc < 29.9) {
        mensagem = " Sobrepeso";
    }else if (Imc < 34.9) {
        mensagem = " Obeso grau I";
    }else if (Imc < 39.9) {
        mensagem =  " Obeso grau II";
    }else {
        mensagem =  " Obeso grau III ou mÃ³rbido";
    }
    
    Lbresp.setText(mensagem);
}

